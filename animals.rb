puts "what is your favorite animal?"

animal = gets.chomp.downcase

if animal == "dog"
	puts "Wooofff"
elsif animal == "cat"
	puts "Meoooewww"
elsif animal == "donkey"
	puts "heeehuuuuunhhh"
elsif animal == "horse"
	puts "neigh"
elsif animal == "lion"
	puts "roar"
elsif animal == "snake"
	puts "hiissssssss"
elsif animal == "bird"
	puts "chirp"
else
	puts "grrrrrrrrr"			
end
